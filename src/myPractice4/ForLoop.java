package myPractice4;

public class ForLoop {

    public static void main(String[] args) {

        for (int i=0; i<=5; i++){
            System.out.println("Hello! How are you doing?"+i);

            for (int p=0; p<2; p++){
                System.out.println("World"+i);
            }
        }
        for (int p=0; p<3; p++){
            System.out.println("World Cup Championship");
        }
    }
}
