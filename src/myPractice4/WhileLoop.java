package myPractice4;

public class WhileLoop {
    public static void main(String[] args) {


        int x = 0;

        while (x<5){
            System.out.println("While loop.");
            x++;
        }

        int y =0;
        while(y<10){
            System.out.println("While learning");
            y++;
        }

        int c=0;

        while (c<5){
            System.out.println("Many many time");
            c++;
        }

    }
}

