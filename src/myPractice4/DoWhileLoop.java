package myPractice4;

public class DoWhileLoop {

    public static void main(String[] args) {

        int x=0;
        do{
            System.out.println("Java do loop");
            x++;
        }while(x<5);

        int y = 0;
        do{
            System.out.println("Again");
            y++;
        }while (y<4);


        int w=0;
        do {
            System.out.println("3rd time do loop conditions");
            w++;
        }while (w<10);


    }


}
